    public static function resolve(
        string $explicitCode = '',
        string $regionName = '',
        string $city = '',
        string $postalCode = '',
        string $addressFull = ''
    ): string {
        $code = self::normalizeCode($explicitCode);
        if ($code !== '') {
            return $code;
        }

        $code = self::matchByName($regionName);
        if ($code !== '') {
            return $code;
        }

        $code = self::matchByName($city);
        if ($code !== '') {
            return $code;
        }

        $code = self::matchByName($addressFull);
        if ($code !== '') {
            return $code;
        }

        return self::matchByPostalCode($postalCode);
    }

    public static function normalizeCode(string $code): string
    {
        $code = trim($code);
        if ($code === '') {
            return '';
        }

        if (preg_match('/^(\d{2})/', $code, $matches)) {
            return $matches[1];
        }

        return '';
    }

    private static function matchByName(string $text): string
    {
        $text = mb_strtolower(trim($text));
        if ($text === '') {
            return '';
        }

        $bestLen = 0;
        $bestCode = '';

        foreach (self::ALIASES as $needle => $code) {
            if ($needle !== '' && str_contains($text, $needle) && mb_strlen($needle) > $bestLen) {
                $bestLen = mb_strlen($needle);
                $bestCode = $code;
            }
        }

        return $bestCode;
    }

    private static function matchByPostalCode(string $postalCode): string
    {
        $postalCode = preg_replace('/\D/', '', $postalCode) ?? '';
        if (strlen($postalCode) < 3) {
            return '';
        }

        $prefix = substr($postalCode, 0, 3);

        return self::POSTAL_PREFIX[$prefix] ?? '';
    }
